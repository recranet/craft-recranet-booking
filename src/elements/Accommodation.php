            ],
            // ...
        ];
    }

    protected static function defineTableAttributes(): array
    {
        return [
            'recranetBookingId' => ['label' => Craft::t('app', 'Recranet Booking ID')],
            'slug' => ['label' => Craft::t('app', 'Slug')],
            'slugDe' => ['label' => Craft::t('app', 'Slug (DE)')],
